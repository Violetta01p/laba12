# Початковий список товарів
inventory = [
    {"назва": "Ноутбук", "кількість": 10, "ціна": 15000, "категорія": "Електроніка"},
    {"назва": "Мишка", "кількість": 25, "ціна": 400, "категорія": "Електроніка"},
    {"назва": "Куртка", "кількість": 7, "ціна": 1200, "категорія": "Одяг"},
    {"назва": "Хліб", "кількість": 30, "ціна": 25, "категорія": "Продукти"},
    {"назва": "Сир", "кількість": 12, "ціна": 110, "категорія": "Продукти"},
    {"назва": "Книга", "кількість": 5, "ціна": 300, "категорія": "Канцелярія"},
    {"назва": "Флешка", "кількість": 2, "ціна": 250, "категорія": "Електроніка"}
]

# Введення ключа для пошуку
key = input("Введіть назву або категорію для пошуку: ").strip()

# Перевірка і пошук
found = False
for item in inventory:
    if key.lower() == item["назва"].lower() or key.lower() == item["категорія"].lower():
        print(f"\nЗнайдено: {item['назва']}, {item['кількість']} шт, {item['ціна']} грн, категорія: {item['категорія']}")
        action = input("Бажаєте оновити 'кількість' чи 'ціну'? Якщо ні — натисніть Enter: ").strip().lower()

        if action == "кількість":
            try:
                new_qty = int(input("Нова кількість: "))
                item["кількість"] = new_qty
                print("Кількість оновлено.")
            except ValueError:
                print("Помилка: введено нечислове значення.")
        elif action == "ціна":
            try:
                new_price = float(input("Нова ціна: "))
                item["ціна"] = new_price
                print("Ціну оновлено.")
            except ValueError:
                print("Помилка: введено нечислове значення.")
        found = True

# Якщо не знайдено жодного товару
if not found:
    print("Товар не знайдено.")
