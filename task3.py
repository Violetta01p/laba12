# Інвентаризація (має бути перед аналітикою)
inventory = [
    {"назва": "Ноутбук", "кількість": 10, "ціна": 15000, "категорія": "Електроніка"},
    {"назва": "Мишка", "кількість": 25, "ціна": 400, "категорія": "Електроніка"},
    {"назва": "Куртка", "кількість": 7, "ціна": 1200, "категорія": "Одяг"},
    {"назва": "Хліб", "кількість": 30, "ціна": 25, "категорія": "Продукти"},
    {"назва": "Сир", "кількість": 12, "ціна": 110, "категорія": "Продукти"},
    {"назва": "Книга", "кількість": 5, "ціна": 300, "категорія": "Канцелярія"},
    {"назва": "Флешка", "кількість": 2, "ціна": 250, "категорія": "Електроніка"}
]

# Обрахунок загальної вартості по категоріях
category_totals = {}
for item in inventory:
    total_value = item["кількість"] * item["ціна"]
    category = item["категорія"]
    if category not in category_totals:
        category_totals[category] = 0
    category_totals[category] += total_value

print("\nЗагальна вартість товарів по категоріях:")
for category, total in category_totals.items():
    print(f"{category}: {total:.2f} грн")

# Знаходження категорії з найбільшою вартістю
max_category = max(category_totals, key=category_totals.get)
print(f"\nКатегорія з найбільшою вартістю: {max_category}")

# Вивід товарів, кількість яких нижча за 5
print("\nТовари, які потребують дозамовлення (менше 5 штук):")
for item in inventory:
    if item["кількість"] < 5:
        print(f"{item['назва']} — {item['кількість']} шт")
